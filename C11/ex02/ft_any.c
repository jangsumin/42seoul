int	ft_any(char **tab, int (*f)(char*))
{
	int	i;
	int	res;

	i = 0;
	res = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]))
		{
			res = 1;
			break ;
		}
		i++;
	}
	return (res);
}
